class Product {
  final String id;
  final String title;
  final String type;
  final String skinType;
  final String effect;
  final String category;
  final double price;
  final String imageUrl;

  Product({required this.id,
    required this.title,
    required this.type,
    required this.skinType,
    required this.effect,
    required this.category,
    required this.price,
    required this.imageUrl});
}
